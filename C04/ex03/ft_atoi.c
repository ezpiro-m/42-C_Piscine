#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	signedd;
	int	number;

	number = 0;
	signedd = 1;
	i = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signedd = signedd * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (str[i] - '0') + (number * 10);
		i++;
	}
	return (number * signedd);
}
