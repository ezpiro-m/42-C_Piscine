#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	e;

i = 0;
	while (i < (size / 2))
	{
		e = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = e;
		i++;
	}
}
