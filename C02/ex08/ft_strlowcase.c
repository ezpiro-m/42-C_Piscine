#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	*ch;

	ch = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
			*str += 32;
		++str;
	}
	return (ch);
}
